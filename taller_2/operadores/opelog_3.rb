# Ejercicio 3: Operadores Logicos

verdadero = true
falso = false


puts (verdadero and verdadero)
puts (verdadero and falso)
puts (falso and falso)

puts (verdadero or verdadero)
puts (verdadero or falso)
puts (falso or falso)

puts (not verdadero)
puts (not falso)

if verdadero == true
	puts 'es verdadero'
elsif
	puts 'es falso'
end
